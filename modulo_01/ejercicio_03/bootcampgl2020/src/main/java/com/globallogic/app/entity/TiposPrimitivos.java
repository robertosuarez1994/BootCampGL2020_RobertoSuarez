package com.globallogic.app.entity;

public class TiposPrimitivos {
	 
	public static void main( String[] args ) {
		int myNum = 15;
		char myChar = 'a';
		boolean myBool = true;
		float myFloat = 2436.2456f;
		short myShort = 500;
		
		System.out.println(String.format("El valor de mi numero es %d", myNum));
		System.out.println(String.format("El valor de mi caracter es %c",myChar));
		System.out.println(String.format("El valor de mi booleano es %b",myBool));
		System.out.println(String.format("El valor de mi float es %f",myFloat));
		System.out.println(String.format("El valor de mi short %d",myShort));
		
	}
	
}
